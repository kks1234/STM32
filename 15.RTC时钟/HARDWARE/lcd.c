//写寄存器命令
void LCD_WR_REG(u16 regval)
{
	LCD->LCD_REG=regval;
}
//写数据
void LCD_WR_DATA(u16 data)
{
	LCD->LCD_RAM=data;
}
//读数据
u16 LCD_RD_DATA()
{
	vu16 temp;
	temp=LCD->LCD_RAM;
	return temp;
}
//写寄存器参数
void LCD_WriteReg(u16 LCD_Reg,u16 RegVal)
{
	LCD->LCD_REG=LCD_Reg;
	LCD->LCD_RAM=RegVal;
}
//读寄存器参数
u16  LCD_ReadReg(u16 LCD_Reg)
{
	LCD_WR_REG(LCD_Reg);
	return LCD_RD_DATA();
	
}

//开始写GRAM指令
void LCD_WriteRam_Start()
{
	LCD->LCD_REG=lcd_dev.wramcmd;
}
//写GRAM颜色RGB数据
void LCD_WriteRam(u16 RGBcode)
{
	LCD->LCD_RAM=RGBcode;
}

//显示开启
void LCD_Display_on()
{
	if(lcd_dev.id==0x5310)
		LCD_WR_REG(0x29);
}
//显示关闭
void LCD_Display_off()
{
	if(lcd_dev.id==0x5310)
		LCD_WR_REG(0x28);
}
//设置坐标
void LCD_SetCursor(u16 Xpos, u16 Ypos)
{
	if(lcd_dev.id==0x5310)
	{
		LCD_WR_REG(lcd_dev.setxcmd);  //设置x坐标指令
		LCD_WR_DATA(Xpos>>8);
		LCD_WR_DATA(Xpos&0x00ff);
		LCD_WR_REG(lcd_dev.setycmd);  //设置y坐标指令
		LCD_WR_DATA(Ypos>>8);
		LCD_WR_DATA(Ypos&0x00ff);
	}
}
//画点函数
void LCD_DrawPoint(u16 x,u16 y)
{
	LCD_SetCursor(x,y);
	LCD_WriteRam_Start(); //开始写GRAM函数
	LCD_WriteRam(PRINT_COLOR); //写GRAM
}
//清屏函数
void LCD_Clear(u16 Color)
{
	u32 num,i;
	num=lcd_dev.height*lcd_dev.width;
	if(lcd_dev.id==0x5310)
		LCD_SetCursor(0,0); //设置坐标
	LCD_WriteRam_Start();
	for(i=0;i<num;i++)
	{
		LCD_WriteRam(Color); 
	}
}
//快速画点函数
void LCD_Fast_DrawPoint(u16 x,u16 y,u16 color)
{
	if(lcd_dev.id==0x5310)
	{
		LCD->LCD_REG=lcd_dev.setxcmd;
		LCD->LCD_RAM=x>>8;
		LCD->LCD_RAM=x&0x00ff;
		LCD->LCD_REG=lcd_dev.setycmd;
		LCD->LCD_RAM=y>>8;
		LCD->LCD_RAM=y&0x00ff;   //设置坐标
	}
	LCD->LCD_REG=lcd_dev.wramcmd;  //画点
	LCD->LCD_RAM=color;
}
//读点函数
u16 LCD_ReadPoint(u16 x,u16 y)
{
	u16 temp;
	u16 r=0,g=0,b=0;
	if(x>lcd_dev.width||y>lcd_dev.height)
		return 0;
	if(lcd_dev.id==0x5310)
	{
		LCD_SetCursor(x,y);
		LCD_WR_REG(0x2e);
		temp=LCD_RD_DATA(); //dummy
		temp=LCD_RD_DATA();
		r=temp&0xff00;
		g=temp&0x00ff;
		temp=LCD_RD_DATA();
		b=temp&0xff00;
	}
	return (r|(g<<3)|(b>>3));
}
//开窗函数
// sx:开始x  sy:开始y 
void LCD_Set_Window(u16 sx,u16 sy,u16 width,u16 height)
{
	LCD->LCD_REG=lcd_dev.setxcmd;
	LCD->LCD_RAM=sx>>8;
	LCD->LCD_RAM=sx&0x00FF;
	LCD->LCD_RAM=(sx+width)>>8;
	LCD->LCD_RAM=(sx+width)&0x00FF;
	LCD->LCD_REG=lcd_dev.setycmd;
	LCD->LCD_RAM=sy>>8;
	LCD->LCD_RAM=sy&0x00FF;
	LCD->LCD_RAM=(sy+height)>>8;
	LCD->LCD_RAM=(sy+height)&0x00FF;
}
//颜色填充
//  sx:开始x  ex：结束x
//  sy:开始y  ey: 结束y
//  color 颜色数据
void LCD_Fill(u16 sx,u16 sy,u16 ex,u16 ey,u16 color)
{
	u16 i,j;
	for(i=sy;i<=ey;i++)
	{
		LCD_SetCursor(sx,i);
		LCD_WriteRam_Start();   
		for(j=0;j<(ex-sx+1);j++)
		{
			LCD_WriteRam(color);   //显示颜色
		}
	}	
}
//设置屏扫描方向设置
// dir 反向
void LCD_Scan_Dir(u8 dir)
{
	u16 regval,reg;
	u16 temp;
	

	if(lcd_dev.dir&&(lcd_dev.id==0x5310))
	switch(dir)
	{
		case L2R_U2D :  dir=D2U_L2R; break;
		case L2R_D2U :  dir=D2U_R2L; break;
		case R2L_U2D :  dir=U2D_L2R; break;
		case R2L_D2U :	dir=U2D_R2L; break;
		case U2D_L2R :	dir=L2R_D2U; break;
		case U2D_R2L :	dir=L2R_U2D; break;
		case D2U_L2R :	dir=R2L_D2U; break;
		case D2U_R2L :	dir=R2L_U2D; break;
	}
	if(lcd_dev.id==0x5310)
	{
		switch(dir)
		{
			case L2R_U2D :  regval=(0<<7)|(0<<6)|(0<<5); break;
			case L2R_D2U :  regval=(1<<7)|(0<<6)|(0<<5); break;
			case R2L_U2D :  regval=(0<<7)|(1<<6)|(0<<5); break;
			case R2L_D2U :	regval=(1<<7)|(1<<6)|(0<<5); break;
			case U2D_L2R :	regval=(0<<7)|(0<<6)|(1<<5); break;
			case U2D_R2L :	regval=(0<<7)|(1<<6)|(1<<5); break;
			case D2U_L2R :	regval=(1<<7)|(0<<6)|(1<<5); break;
			case D2U_R2L :	regval=(1<<7)|(1<<6)|(1<<5); break;	
		}
	}
	if(lcd_dev.id==0x5310)
	{
		reg=0x36;
	}
	LCD_WriteReg(reg,regval);
	if(regval&0x20)
	{
		if(lcd_dev.width<lcd_dev.height)
		{
			temp=lcd_dev.width;
			lcd_dev.width=lcd_dev.height;
			lcd_dev.height=temp;
		}
	}
	else
	{
		if(lcd_dev.width>lcd_dev.height)
		{
			temp=lcd_dev.width;
			lcd_dev.width=lcd_dev.height;
			lcd_dev.height=temp;
		}
	}	
}
//设置屏幕显示方向
// dir: 1反向
void LCD_Display_Dir(u8 dir)
{
	if(dir==0)  
	{
		lcd_dev.dir=0;    //正向
		if(lcd_dev.id==0x5310)
		{
			lcd_dev.height=480;
			lcd_dev.width=320;
			lcd_dev.setxcmd=0x2A;
			lcd_dev.setycmd=0x2B;
			lcd_dev.wramcmd=0x2C;	
		}
	}
	else
	{
		lcd_dev.dir=1;   //反向
		if(lcd_dev.id==0x5310)
		{
			lcd_dev.height=320;
			lcd_dev.width=480;
			lcd_dev.setxcmd=0x2A;
			lcd_dev.setycmd=0x2B;
			lcd_dev.wramcmd=0x2C;	
		}
	}
	LCD_Scan_Dir(TFT_SCAN);
}

//在指定位置画一个指定大小的圆
//(x,y):中心点
//r    :半径
void LCD_Draw_Circle(u16 x0,u16 y0,u8 r)
{
	int a,b;
	int di;
	a=0;b=r;	  
	di=3-(r<<1);             //判断下个点位置的标志
	while(a<=b)
	{
		LCD_DrawPoint(x0+a,y0-b);             //5
 		LCD_DrawPoint(x0+b,y0-a);             //0           
		LCD_DrawPoint(x0+b,y0+a);             //4               
		LCD_DrawPoint(x0+a,y0+b);             //6 
		LCD_DrawPoint(x0-a,y0+b);             //1       
 		LCD_DrawPoint(x0-b,y0+a);             
		LCD_DrawPoint(x0-a,y0-b);             //2             
  		LCD_DrawPoint(x0-b,y0-a);             //7     	         
		a++;
		//使用Bresenham算法画圆     
		if(di<0)di +=4*a+6;	  
		else
		{
			di+=10+4*(a-b);   
			b--;
		} 						    
	}
} 									  

//画直线
void LCD_DrawLine(u16 x1, u16 y1, u16 x2, u16 y2)
{
	int xlen,ylen,flag,i;
	int xt,yt;
	u16 x=x1,y=y1;
	int xeer=0,yeer=0;
	
	xlen=x2-x1;
	ylen=y2-y1;
	if(xlen>0)
		xt=1;
	else if(xlen==0)
		xt=0;
	else
	{
		xt=-1;
		xlen=-xlen;
	}
	if(ylen>0)
		yt=1;
	else if(ylen==0)
		yt=0;
	else
	{
		yt=-1;
		ylen=-ylen;
	}
	if(xlen>ylen)
		flag=xlen;
	else
		flag=ylen;
	for(i=0;i<=flag+1;i++)
	{
		LCD_DrawPoint(x,y);
		xeer+=xlen;
		yeer+=ylen;
		if(xeer>flag)
		{
			xeer-=flag;
			x+=xt;
		}
		if(yeer>flag)
		{
			yeer-=flag;
			y+=yt;
		}
	}
}	

